class FeedbackCreate(BaseModel):
    """
    Create feedback request.
    Gemini auto-detects: sentiment and sentiment_respon (via RAG context).
    Category is user-provided.
    """
    text: str
    customer_id: Optional[str] = None
    product_id: Optional[str] = None
    source: str = "web"
    rating: Optional[int] = None
    category: Optional[str] = None

    class Config:
        json_schema_extra = {
            "example": {
                "text": "Pengiriman sangat lambat dan barang datang dalam kondisi rusak",
                "customer_id": "cust_123",
                "product_id": "prod_456",
                "source": "website",
                "rating": 2,
                "category": "delivery"
            }
        }


class FeedbackUpdate(BaseModel):
    """Update feedback request"""
    text: Optional[str] = None
    category: Optional[str] = None
    rating: Optional[int] = None
    is_verified: Optional[bool] = None


class FeedbackResponse(BaseModel):
    """
    Feedback response model.

    Auto-detected fields dari Gemini (with RAG context):
    - sentiment       → positive / neutral / negative
    - sentiment_respon → personalized response generated by Gemini

    User-provided fields:
    - category  → diisi saat submit feedback
    """
    id: int
    text: str
    customer_id: Optional[str]
    product_id: Optional[str]
    sentiment: str
    sentiment_respon: Optional[str]
    category: Optional[str]
    rating: Optional[int]
    source: Optional[str]
    created_at: datetime

    class Config:
        from_attributes = True


# ---------------------------------------------------------------------------
# Helper: Build RAG context string
# ---------------------------------------------------------------------------

def _build_rag_context(similar_feedbacks: List[dict]) -> Optional[str]:
    """
    Format similar feedbacks from RAG into a context string for Gemini.
    Returns None if no relevant feedbacks found.
    """
    if not similar_feedbacks:
        return None

    lines = []
    for i, item in enumerate(similar_feedbacks[:5], 1):
        text = item.get('text', '')[:150]
        sentiment = item.get('metadata', {}).get('sentiment', '?')
        lines.append(f"{i}. [{sentiment.upper()}] {text}")

    return "Similar past feedbacks:\n" + "\n".join(lines)


