@router.get("/search", response_model=List[FeedbackResponse])
async def search_feedback(
    query: str = Query(..., min_length=1),
    top_k: int = Query(5, ge=1, le=20),
    db: AsyncSession = Depends(get_db)
):
    """Semantic search for similar feedbacks using RAG"""
    try:
        rag = get_rag_pipeline()
        results = rag.similarity_search(query, top_k=top_k)

        feedback_ids = [r['feedback_id'] for r in results]
        if not feedback_ids:
            return []

        query_stmt = select(Feedback).where(Feedback.id.in_(feedback_ids))
        result = await db.execute(query_stmt)
        feedbacks = result.scalars().all()

        feedback_dict = {f.id: f for f in feedbacks}
        return [feedback_dict[fid] for fid in feedback_ids if fid in feedback_dict]

    except Exception as e:
        logger.error(f"Error searching feedback: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


@router.get("/stats/summary")
async def feedback_summary(
    days: int = Query(7, ge=1),
    date: str = Query(None),
    db: AsyncSession = Depends(get_db)
):
    try:
        # kalau ada parameter date, filter spesifik tanggal itu aja
        if date:
            target_date = datetime.strptime(date, "%Y-%m-%d")
            since = target_date.replace(hour=0, minute=0, second=0)
            until = target_date.replace(hour=23, minute=59, second=59)
            date_filter = (Feedback.created_at >= since) & (Feedback.created_at <= until)
        else:
            since = datetime.utcnow() - timedelta(days=days)
            date_filter = Feedback.created_at >= since

        total_result = await db.execute(
            select(func.count(Feedback.id)).where(date_filter)
        )
        total = total_result.scalar()

        sentiment_result = await db.execute(
            select(Feedback.sentiment, func.count(Feedback.id))
            .where(date_filter)
            .group_by(Feedback.sentiment)
        )
        sentiment_counts = {row[0].value if hasattr(row[0], 'value') else row[0]: row[1]
                            for row in sentiment_result}

        category_result = await db.execute(
            select(Feedback.category, func.count(Feedback.id))
            .where(date_filter)
            .group_by(Feedback.category)
        )
        category_counts = {row[0] or 'other': row[1] for row in category_result}

        rating_result = await db.execute(
            select(func.avg(Feedback.rating)).where(
                date_filter,
                Feedback.rating.isnot(None)
            )
        )
        avg_rating = rating_result.scalar() or 0

        return {
            "period_days": 1 if date else days,
            "total_feedbacks": total,
            "sentiment_distribution": sentiment_counts,
            "category_distribution": category_counts,
            "average_rating": round(float(avg_rating), 2),
            "timestamp": datetime.utcnow().isoformat()
        }

    except Exception as e:
        logger.error(f"Error generating summary: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


