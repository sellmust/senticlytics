@router.post("", response_model=FeedbackResponse, status_code=201)
async def create_feedback(
    feedback: FeedbackCreate,
    db: AsyncSession = Depends(get_db)
):
    """
    Create new feedback with AI analysis.

    Flow:
    1. RAG retrieves similar past feedbacks as context
    2. Gemini analyzes sentiment AND generates personalized sentiment_respon
       using the retrieved context — not a template
    3. Result is stored and indexed for future RAG queries
    """
    try:
        gemini = await get_gemini_service()
        rag = get_rag_pipeline()

        # Step 1: Retrieve similar feedbacks from RAG for context
        try:
            similar = rag.similarity_search(feedback.text, top_k=5)
            rag_context = _build_rag_context(similar)
        except Exception as e:
            logger.warning(f"⚠ RAG context retrieval failed: {str(e)}")
            rag_context = None

        # Step 2: Gemini analyzes sentiment + generates response using RAG context
        analysis = await gemini.analyze_feedback(feedback.text, rag_context=rag_context)

        new_feedback = Feedback(
            text=feedback.text,
            customer_id=feedback.customer_id,
            product_id=feedback.product_id,
            source=feedback.source,
            rating=feedback.rating,
            category=feedback.category,

            # Hasil dari Gemini
            sentiment=analysis.get('sentiment', 'neutral'),
            sentiment_respon=analysis.get('sentiment_respon', 'Terima kasih atas ulasan Anda.'),
        )

        db.add(new_feedback)
        await db.flush()

        # Step 3: Index new feedback into RAG
        rag.index_feedback(
            feedback_id=new_feedback.id,
            text=feedback.text,
            metadata={
                'sentiment':   new_feedback.sentiment,
                'category':    new_feedback.category,
                'source':      feedback.source,
                'customer_id': feedback.customer_id,
            }
        )

        await db.commit()
        await db.refresh(new_feedback)

        logger.info(
            f"✓ Created feedback {new_feedback.id} | "
            f"sentiment={new_feedback.sentiment} | "
            f"category={new_feedback.category} | "
            f"rag_context={'yes' if rag_context else 'no'}"
        )
        return new_feedback

    except Exception as e:
        await db.rollback()
        logger.error(f"❌ Error creating feedback: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


@router.get("/{feedback_id}", response_model=FeedbackResponse)
async def get_feedback(
    feedback_id: int,
    db: AsyncSession = Depends(get_db)
):
    """Get specific feedback by ID"""
    try:
        result = await db.execute(
            select(Feedback).where(Feedback.id == feedback_id)
        )
        feedback = result.scalar_one_or_none()
        if not feedback:
            raise HTTPException(status_code=404, detail="Feedback not found")
        return feedback
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error getting feedback: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


@router.get("", response_model=List[FeedbackResponse])
async def list_feedbacks(
    skip: int = Query(0, ge=0),
    limit: int = Query(10, ge=1, le=100),
    sentiment: Optional[str] = None,
    category: Optional[str] = None,
    days: int = Query(7, ge=1),
    db: AsyncSession = Depends(get_db)
):
    """
    List feedbacks with filters.

    Query Parameters:
    - skip      : pagination offset
    - limit     : results per page (max 100)
    - sentiment : positive | neutral | negative
    - category  : kategori yang diisi user saat submit
    - days      : feedbacks from last N days (default 7)
    """
    try:
        query = select(Feedback)

        since = datetime.utcnow() - timedelta(days=days)
        query = query.where(Feedback.created_at >= since)

        if sentiment:
            try:
                sentiment_enum = SentimentType(sentiment)
                query = query.where(Feedback.sentiment == sentiment_enum)
            except ValueError:
                raise HTTPException(status_code=400, detail=f"Invalid sentiment: {sentiment}")

        if category:
            query = query.where(Feedback.category == category)

        query = query.order_by(desc(Feedback.created_at)).offset(skip).limit(limit)

        result = await db.execute(query)
        return result.scalars().all()

    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error listing feedbacks: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


@router.put("/{feedback_id}", response_model=FeedbackResponse)
async def update_feedback(
    feedback_id: int,
    update: FeedbackUpdate,
    db: AsyncSession = Depends(get_db)
):
    """Update feedback (manual override only)"""
    try:
        result = await db.execute(
            select(Feedback).where(Feedback.id == feedback_id)
        )
        feedback = result.scalar_one_or_none()
        if not feedback:
            raise HTTPException(status_code=404, detail="Feedback not found")

        if update.text:
            feedback.text = update.text
        if update.category:
            feedback.category = update.category
        if update.rating is not None:
            feedback.rating = update.rating
        if update.is_verified is not None:
            feedback.is_verified = update.is_verified

        feedback.updated_at = datetime.utcnow()

        await db.commit()
        await db.refresh(feedback)
        logger.info(f"✓ Updated feedback {feedback_id}")
        return feedback

    except HTTPException:
        raise
    except Exception as e:
        await db.rollback()
        logger.error(f"Error updating feedback: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


@router.delete("/{feedback_id}", status_code=204)
async def delete_feedback(
    feedback_id: int,
    db: AsyncSession = Depends(get_db)
):
    """Delete feedback"""
    try:
        result = await db.execute(
            select(Feedback).where(Feedback.id == feedback_id)
        )
        feedback = result.scalar_one_or_none()
        if not feedback:
            raise HTTPException(status_code=404, detail="Feedback not found")

        rag = get_rag_pipeline()
        rag.delete_feedback(feedback_id)

        await db.delete(feedback)
        await db.commit()
        logger.info(f"✓ Deleted feedback {feedback_id}")

    except HTTPException:
        raise
    except Exception as e:
        await db.rollback()
        logger.error(f"Error deleting feedback: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))


