@router.get("/report/export")
async def export_report(
    days: int = Query(7, ge=1),
    date: str = Query(None),
    sentiment: Optional[str] = None,
    category: Optional[str] = None,
    db: AsyncSession = Depends(get_db)
):
    """
    Export feedback report untuk n8n pipeline.

    Returns flat list dengan:
    - id, customer_id, rating, text
    - sentiment, sentiment_respon
    - category, source, created_at
    """
    try:
        if date:
            target_date = datetime.strptime(date, "%Y-%m-%d")
            since = target_date.replace(hour=0, minute=0, second=0)
            until = target_date.replace(hour=23, minute=59, second=59)
            date_filter = (Feedback.created_at >= since) & (Feedback.created_at <= until)
        else:
            since = datetime.utcnow() - timedelta(days=days)
            date_filter = Feedback.created_at >= since

        query = select(Feedback).where(date_filter)

        if sentiment:
            query = query.where(Feedback.sentiment == sentiment)
        if category:
            query = query.where(Feedback.category == category)

        query = query.order_by(desc(Feedback.created_at))
        result = await db.execute(query)
        feedbacks = result.scalars().all()

        rows = []
        for f in feedbacks:
            rows.append({
                "id": f.id,
                "customer_id": f.customer_id or "-",
                "product_id": f.product_id or "-",
                "rating": f.rating,
                "text": f.text,
                "sentiment": f.sentiment.value if hasattr(f.sentiment, 'value') else f.sentiment,
                "sentiment_respon": f.sentiment_respon or "-",
                "category": f.category or "-",
                "source": f.source or "-",
                "created_at": f.created_at.isoformat() if f.created_at else "-",
            })

        return {
            "period_days": 1 if date else days,
            "total": len(rows),
            "generated_at": datetime.utcnow().isoformat(),
            "feedbacks": rows
        }

    except Exception as e:
        logger.error(f"Error exporting report: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))